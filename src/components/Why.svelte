<h2>Why Fuel Cells?</h2>
<p>
        As the world transitions away from fossil fuels to combat climate change, there 
    needs to be other production methods to produce energy with lower carbon dioxide emmissions to fuel growing energy requirements.
    Fuel cells can be an efficient way of providing energy without a large carbon footprint.
    A hydrogen fuel cell's output is only water vapor, an attractive alternative for clean energy vehicles looking to reduce emissions.
    (<b>Fun fact:</b> NASA used hydrogen fuel cells in their spacecrafts in the 1960's!)
    Furthermore, by performing <a href=https://en.wikipedia.org/wiki/Electrolysis><b>electrolysis</b></a>, the energy-intensive splitting of water, hydrogen
    can be produced freely if energy is obtained from a clean source.
</p>
<p>
    Therefore, it becomes necessary to have tools to properly characterize a fuel cell in order to design an efficient one.
    Enter, the current-density vs. voltage curve! Also known as a polarization curve, it tells a story of a fuel cell's effectiveness.
</p>
